export function getThemeById(id: string): ThemePreset | undefined {
  return THEMES.find((t) => t.id === id)
}

/** Generate inline CSS style string for theme overrides */
export function getThemeStyleString(themeId: string): string {
  const theme = getThemeById(themeId)
  if (!theme || theme.id === "yellow") return ""

  const lightVars = Object.entries(theme.light)
    .map(([k, v]) => `${k}:${v}`)
    .join(";")

  const darkVars = Object.entries(theme.dark)
    .map(([k, v]) => `${k}:${v}`)
    .join(";")

  if (!lightVars && !darkVars) return ""

  return `<style>:root{${lightVars}}.dark{${darkVars}}</style>`
}
